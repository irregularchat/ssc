/**
 * Batch compute Plus Codes (Open Location Code) for all buildings.
 *
 * Usage:
 *   1. Export buildings (reuse /tmp/buildings_coords.json from MGRS batch)
 *   2. node scripts/batch-plus-code-update.mjs
 *   3. npx wrangler d1 execute milnav-db --remote --file=migrations/0007_populate_plus_codes.sql
 */
import { readFileSync, writeFileSync } from 'fs'
import { OpenLocationCode } from 'open-location-code'

const olc = new OpenLocationCode()
const inputFile = '/tmp/buildings_coords.json'
const outputFile = '/Users/sac/Git/milnav/migrations/0007_populate_plus_codes.sql'

console.log(`Reading buildings from ${inputFile}...`)
const raw = JSON.parse(readFileSync(inputFile, 'utf-8'))
const buildings = raw[0].results

console.log(`Found ${buildings.length} buildings`)

let sql = '-- Batch Plus Code update for all buildings\n'
sql += '-- Generated by scripts/batch-plus-code-update.mjs\n'
sql += `-- Generated: ${new Date().toISOString()}\n\n`

let converted = 0
let skipped = 0

for (const b of buildings) {
  if (!b.latitude || !b.longitude || isNaN(b.latitude) || isNaN(b.longitude)) {
    skipped++
    continue
  }
  // encode(lat, lng, codeLength) — 10 digits = ~14m x 14m (standard Google format)
  // NOTE: length 11 adds grid refinement that Google Maps doesn't handle well
  const code = olc.encode(b.latitude, b.longitude, 10)
  sql += `UPDATE buildings SET plus_code = '${code}' WHERE id = '${b.id}';\n`
  converted++
}

writeFileSync(outputFile, sql)

console.log(`\nResults:`)
console.log(`  Converted: ${converted}`)
console.log(`  Skipped: ${skipped}`)
console.log(`\nSQL written to: ${outputFile}`)
