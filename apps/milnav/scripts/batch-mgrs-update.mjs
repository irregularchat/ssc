/**
 * Batch compute MGRS for all buildings and generate SQL UPDATE statements.
 *
 * Usage:
 *   1. Export buildings from D1:
 *      npx wrangler d1 execute milnav-db --remote --command="SELECT id, latitude, longitude FROM buildings;" --json > /tmp/buildings_coords.json
 *   2. Run this script:
 *      node scripts/batch-mgrs-update.mjs
 *   3. Apply the generated SQL:
 *      npx wrangler d1 execute milnav-db --remote --file=migrations/0005_populate_mgrs.sql
 */
import { readFileSync, writeFileSync } from 'fs'
import pkg from 'mgrs'
const { forward } = pkg

const inputFile = '/tmp/buildings_coords.json'
const outputFile = '/Users/sac/Git/milnav/migrations/0005_populate_mgrs.sql'

console.log(`Reading buildings from ${inputFile}...`)
const raw = JSON.parse(readFileSync(inputFile, 'utf-8'))

// wrangler d1 --json output is an array with one result object
const buildings = raw[0].results

console.log(`Found ${buildings.length} buildings`)

let sql = '-- Batch MGRS coordinate update for all buildings\n'
sql += '-- Generated by scripts/batch-mgrs-update.mjs\n'
sql += `-- Generated: ${new Date().toISOString()}\n\n`

let converted = 0
let skipped = 0
let errors = 0

for (const b of buildings) {
  if (!b.latitude || !b.longitude || isNaN(b.latitude) || isNaN(b.longitude)) {
    skipped++
    continue
  }

  try {
    // CRITICAL: forward() takes [longitude, latitude] order
    const mgrs = forward([b.longitude, b.latitude], 5)
    sql += `UPDATE buildings SET mgrs = '${mgrs}' WHERE id = '${b.id}';\n`
    converted++
  } catch (err) {
    console.error(`Error converting building ${b.id} (${b.latitude}, ${b.longitude}): ${err.message}`)
    errors++
  }
}

writeFileSync(outputFile, sql)

console.log(`\nResults:`)
console.log(`  Converted: ${converted}`)
console.log(`  Skipped (no coords): ${skipped}`)
console.log(`  Errors: ${errors}`)
console.log(`  Total: ${buildings.length}`)
console.log(`\nSQL written to: ${outputFile}`)
console.log(`\nNext step: npx wrangler d1 execute milnav-db --remote --file=${outputFile}`)
